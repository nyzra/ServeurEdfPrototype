  function inverserNomPrenom(str) {
    const nomPrenom = str.split(" ");
    if(nomPrenom.length>1){
      let nom = nomPrenom.find((np) => np === np.toUpperCase());
      let prenom = nomPrenom.find((np) => np !== nom).toLowerCase();
  
      if (nom && prenom) {
        return `${nom} ${prenom}`
      } else {
       return str
      }
    } else {
      return str
     }
    
  }
